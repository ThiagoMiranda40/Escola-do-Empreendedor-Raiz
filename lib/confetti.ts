import confetti from 'canvas-confetti';

// Keep these for future use in the student area
export const SOUNDS = {
    SUCCESS_LEVEL_UP: '/sounds/storegraphic-crowd-cheers-314919.mp3', // Guardado para o aluno
    CROWD_CHEER: '/sounds/Aplauses.mp3',                             // Sua ovação oficial!
};

export const triggerSuccessConfetti = () => {
    // Play real human celebration sound
    const audio = new Audio(SOUNDS.CROWD_CHEER);
    audio.volume = 0.8;
    audio.play().catch(e => console.log('Audio playback failed:', e));

    // Multiple bursts for a bigger celebration
    const duration = 3 * 1000;
    const animationEnd = Date.now() + duration;
    const defaults = { startVelocity: 30, spread: 360, ticks: 60, zIndex: 9999 };

    const randomInRange = (min: number, max: number) => Math.random() * (max - min) + min;

    const interval: any = setInterval(function () {
        const timeLeft = animationEnd - Date.now();

        if (timeLeft <= 0) {
            return clearInterval(interval);
        }

        const particleCount = 50 * (timeLeft / duration);
        confetti({ ...defaults, particleCount, origin: { x: randomInRange(0.1, 0.3), y: Math.random() - 0.2 } });
        confetti({ ...defaults, particleCount, origin: { x: randomInRange(0.7, 0.9), y: Math.random() - 0.2 } });
    }, 250);
};

